    @Test
    public void testContentEquals() throws FileNotFoundException {
        //对比两个Reader的内容是否一致
        File file = new File(this.getClass().getResource("/test.txt").getPath());
        File file1 = new File(this.getClass().getResource("/test1.txt").getPath());
        FileInputStream fileInputStream = new FileInputStream(file);
        FileInputStream fileInputStream1 = new FileInputStream(file1);
        assertFalse(IOKit.contentEquals(fileInputStream, fileInputStream1));

        FileReader reader = new FileReader(file);
        FileReader reader1 = new FileReader(file);
        assertTrue(IOKit.contentEquals(reader, reader1));
    }

